function translateReportPerm(en) {
  let s = en;
  const reps = [
    ['Management reports: open module (any granted section)', 'Rapports de gestion : ouvrir le module (section accordée)'],
    ['Management reports: full director suite (all sections & cards)', 'Rapports de gestion : suite direction complète'],
    ['Management reports: entire Daily tab', 'Rapports de gestion : onglet Quotidien complet'],
    ['Management reports: entire Weekly tab', 'Rapports de gestion : onglet Hebdomadaire complet'],
    ['Management reports: entire Monthly tab', 'Rapports de gestion : onglet Mensuel complet'],
    ['Management reports: entire Financial tab', 'Rapports de gestion : onglet Financier complet'],
    ['Report card: ', 'Carte rapport : '],
    ['Financial report: ', 'Rapport financier : '],
    ['Financial unit: ', 'Unité financière : '],
    [' (daily)', ' (quotidien)'],
    [' (weekly)', ' (hebdomadaire)'],
    [' (monthly)', ' (mensuel)'],
    [' (financial)', ' (financier)'],
    ['Pharmacy', 'Pharmacie'],
    ['Laboratory', 'Laboratoire'],
    ['Radiology', 'Radiologie'],
    ['Emergency', 'Urgences'],
    ['Patient census', 'Recensement patients'],
    ['OPD & emergency', 'OPD et urgences'],
    ['Theatre & procedures', 'Bloc opératoire et actes'],
    ['Clinical alerts', 'Alertes cliniques'],
  ];
  for (const [from, to] of reps) {
    if (s.includes(from)) s = s.split(from).join(to);
  }
  return s;
}

function buildCatalog() {
  const { ALL_REPORT_PERMISSIONS } = require('./hmsDirectorReportsCatalog');
  const modulesEn = {};
  const modulesFr = {};
  for (const [code, label] of ACL_MODULES) {
    modulesEn[code] = label;
    modulesFr[code] = FR_MODULES[code] || label;
  }
  const permsEn = {};
  const permsFr = {};
  for (const [code, label] of ACL_PERMS) {
    permsEn[code] = label;
    permsFr[code] = FR_PERMS[code] || label;
  }
  for (const [code, label] of ALL_REPORT_PERMISSIONS) {
    permsEn[code] = label;
    permsFr[code] = translateReportPerm(label);
  }
  return { modulesEn, modulesFr, permsEn, permsFr };
}

